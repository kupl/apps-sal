def get_age(age):

    for i in age:
        if i == "2":
            return 2
        elif i == "4":
            return 4
        elif i == "5":
            return 5
        elif i == "1":
            return 1
        elif i == "3":
            return 3
        elif i == "6":
            return 6
        elif i == "8":
            return 8
        elif i == "7":
            return 7
        else:
            return 9
