def replace_exclamation(s):
    s = s.replace("i", "!")
    s = s.replace("a", "!")
    s = s.replace("e", "!")
    s = s.replace("o", "!")
    s = s.replace("u", "!")
    s = s.replace("I", "!")
    s = s.replace("A", "!")
    s = s.replace("E", "!")
    s = s.replace("O", "!")
    s = s.replace("U", "!")
    return s
