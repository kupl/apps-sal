def getVolumeOfCubiod(length, width, height):

    # =============================================================================
    #     This function calculates the volume of a cuboid with three values:
    #     length, width and the height of the cuboid
    #
    #     Example:
    #
    #         getVolumeOfCubiod(1, 2, 2) ==> 4
    # =============================================================================

    return(length * width * height)
