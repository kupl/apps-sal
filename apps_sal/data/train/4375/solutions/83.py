def get_planet_name(id):
    case = id
    if case == 1:
        name = "Mercury"
    elif case == 2:
        name = "Venus"
    elif case == 3:
        name = "Earth"
    elif case == 4:
        name = "Mars"
    elif case == 5:
        name = "Jupiter"
    elif case == 6:
        name = "Saturn"
    elif case == 7:
        name = "Uranus"
    elif case == 8:
        name = "Neptune"
    return name
