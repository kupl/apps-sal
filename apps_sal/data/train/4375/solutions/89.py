def get_planet_name(id):
    name = ""
    if id == 1:
        name = "Mercury"
        return name
    if id == 2:
        name = "Venus"
        return name
    if id == 3:
        name = "Earth"
        return name
    if id == 4:
        name = "Mars"
        return name
    if id == 5:
        name = "Jupiter"
        return name
    if id == 6:
        name = "Saturn"
        return name
    if id == 7:
        name = "Uranus"
        return name
    if id == 8:
        name = "Neptune"
        return name
