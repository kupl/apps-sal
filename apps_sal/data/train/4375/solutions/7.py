def get_planet_name(id):
    name = ''
    if id == 1:
        name = 'Mercury'
    if id == 2:
        name = 'Venus'
    if id == 3:
        name = 'Earth'
    if id == 4:
        name = 'Mars'
    if id == 5:
        name = 'Jupiter'
    if id == 6:
        name = 'Saturn'
    if id == 7:
        name = 'Uranus'
    if id == 8:
        name = 'Neptune'
    if id == 9:
        name = 'Pluto'
    return name
