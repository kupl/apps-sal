def get_planet_name(num):
    if num == 1:
        return 'Mercury'
    elif num == 2:
        return 'Venus'
    elif num == 3:
        return 'Earth'
    elif num == 4:
        return 'Mars'
    elif num == 5:
        return 'Jupiter'
    elif num == 6:
        return 'Saturn'
    elif num == 7:
        return 'Uranus'
    elif num == 8:
        return 'Neptune'
    elif num == 9:
        return 'Pluto'
    else:
        return 'Not in range'
