for t in range(int(input())):
    n = int(input())
    c = 0
    e = 0
    d = 0
    o = 0
    h = 0
    f = 0
    for i in range(n):
        s = input()
        c = c + s.count('c')
        e = e + s.count('e')
        d += s.count('d')
        o += s.count('o')
        h += s.count('h')
        f += s.count('f')
    c = c // 2
    e = e // 2
    print(min(c, e, d, o, h, f))
