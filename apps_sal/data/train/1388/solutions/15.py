for _ in range(int(input())):
    n = int(input())
    if n <= 250000:
        print(n)
    elif 250000 < n <= 500000:
        print(int(n - ((n - 250000) * 0.05)))
    elif 500000 < n <= 750000:
        print(int(n - (((n - 500000) * 0.1) + 12500)))
    elif 750000 < n <= 1000000:
        print(int(n - (((n - 750000) * 0.15) + 12500 + 25000)))
    elif 1000000 < n <= 1250000:
        print(int(n - (((n - 1000000) * 0.2) + 12500 + 25000 + 37500)))
    elif 1250000 < n <= 1500000:
        print(int(n - (((n - 1250000) * 0.25) + 12500 + 25000 + 37500 + 50000)))
    else:
        print(int(n - (((n - 1500000) * 0.3) + 12500 + 25000 + 37500 + 50000 + 62500)))
