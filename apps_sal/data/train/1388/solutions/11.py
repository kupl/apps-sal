t = int(input())
for _ in range(t):
    n = int(input())
    ans = 0
    if n > 250000:
        if n <= 500000:
            ans += (n - 250000) * 5 // 100
        else:
            ans += (500000 - 250000) * 5 // 100
    if n > 500000:
        if n <= 750000:
            ans += (n - 500000) * 10 // 100
        else:
            ans += (750000 - 500000) * 10 // 100
    if n > 750000:
        if n <= 1000000:
            ans += (n - 750000) * 15 // 100
        else:
            ans += (1000000 - 750000) * 15 // 100
    if n > 1000000:
        if n <= 1250000:
            ans += (n - 1000000) * 20 // 100
        else:
            ans += (1250000 - 1000000) * 20 // 100
    if n > 1250000:
        if n <= 1500000:
            ans += (n - 1250000) * 25 // 100
        else:
            ans += (1500000 - 1250000) * 25 // 100
    if n > 1500000:
        ans += (n - 1500000) * 30 // 100
    print(n - ans)
