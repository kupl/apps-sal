def print_number(number, char): 
    s1, s2, l = '.'*40, f".{' '*38}.", list(map(int, f"{number:05}"))
    return '\n'.join([s1, s2] + [f".  {' '.join(L[x][i] for x in l)}  ." for i in range(6)] + [s2, s1]).replace('.', char)
