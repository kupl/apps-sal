def print_number(number, char):
    M = [[char for _ in range(40)] for _ in range(10)]
    for i in range(1, 39):
        M[1][i] = ' '
        M[8][i] = ' '
    for i in range(2, 8):
        for j in [1, 2, 9, 16, 23, 30, 37, 38]:
            M[i][j] = ' '
    N = str(number).zfill(5)
    P = [(2, 3), (2, 10), (2, 17), (2, 24), (2, 31)]
    for i in range(5):
        (r, c) = P[i]
        modify(M, int(N[i]), r, c)
    o = ''
    for e in M:
        o += ''.join(e) + '\n'
    return o[:-1]
