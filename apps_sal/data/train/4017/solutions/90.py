def add(a, b):
    add = a + b
    return add


def multiply(a, b):
    num = a * b
    return num


def divide(a, b):
    num = a / b
    return num


def mod(a, b):
    num = a % b
    return num


def exponent(a, b):
    num = a ** b
    return num


def subt(a, b):
    num = a - b
    return num
