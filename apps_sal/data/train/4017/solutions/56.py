def add(numeroUno, numeroDos):

    return numeroUno + numeroDos


def multiply(numeroUno, numeroDos):

    return numeroUno * numeroDos


def divide(numeroUno, numeroDos):

    return numeroUno / numeroDos


def mod(numeroUno, numeroDos):

    return numeroUno % numeroDos


def exponent(numeroUno, numeroDos):

    return numeroUno ** numeroDos


def subt(numeroUno, numeroDos):

    return numeroUno - numeroDos

