def add(numUno,numDos):

  return numUno + numDos


def multiply(numUno,numDos):

  return numUno * numDos


def divide(numUno,numDos):

  return numUno / numDos
  
def mod(numUno,numDos):

  return numUno % numDos


def exponent(numUno,numDos):

  return numUno ** numDos


def subt(numUno,numDos):

  return numUno - numDos
    
    
