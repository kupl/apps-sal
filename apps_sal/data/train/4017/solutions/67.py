from math import *
def add(a, b):
    print(a+b)
    return a+b
def multiply(a, b):
    print(a*b)
    return a*b
def divide(a, b):
    print(a/b)
    return a/b
def mod(a, b):
    print(a%b)
    return a%b
def exponent(a, b):
    print(a^b)
    return a ** b
def subt(a, b):
    print(a-b)
    return a-b
