def add(a, b):
    return a + b
    
def multiply(a, b):
    return a * b

def divide(a, b):
    try:
        return a / b
    except:
        return 0

def mod(a, b):
    try:
        return a % b
    except:
        return 0

def exponent(a, b):
    return a ** b

def subt(a, b):
    return a - b

