def add(a, b):
    add = a + b
    return add


def multiply(a, b):
    multi = a * b
    return multi


def divide(a, b):
    divd = a / b
    return divd


def mod(a, b):
    modu = a % b
    return modu


def exponent(a, b):
    exp = a ** b
    return exp


def subt(a, b):
    subtraction = a - b
    return subtraction
