def add(a,b):
    
    """Addition."""
    
    return a + b
    
def multiply(a,b):

    """Multiplication."""
    
    return a * b
    
def divide(a, b):

    """Division."""
    
    return a / b
    
def mod(a, b):

    """Modulus."""
    
    return a % b
    
def exponent(a, b):

    """Exponential."""
    
    return a ** b
    
def subt(a, b):

    """Subtraction."""
    
    return a -b
