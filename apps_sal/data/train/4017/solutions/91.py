def add(a,b):
    plus = a + b
    return plus

def multiply(a,b):
    times = a * b
    return times

def divide(a,b):
    divs = a / b
    return divs

def mod (a,b):
    mods = a % b
    return mods

def exponent(a,b):
    expos = a ** b
    return expos

def subt(a,b):
    subs = a -b
    return subs
