def basic_op(operator, value1, value2):
    # operator = string
    # value1 = num
    # value2 = num

    if (operator == '*'):
        return value1 * value2
    elif (operator == '+'):
        return value1 + value2
    elif (operator == '-'):
        return value1 - value2
    elif (operator == '/'):
        return value1 / value2
