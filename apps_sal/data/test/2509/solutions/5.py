#!/usr/bin/env python3
import sys


def f(n, N, K):
    '''
    >>> f(2, 10, 2)
    0
    >>> f(1, 10, 0)
    10
    >>> f(1, 10, 1)
    0
    >>> f(2, 10, 1) # 1, 3, 5, 7, 9
    5
    >>> f(3, 10, 0) 
    10
    >>> f(3, 10, 1) # 1, 2, 4, 5, 7, 8, 10
    7
    >>> f(3, 10, 2) # 2, 5, 8
    3
    >>> f(5, 5, 2)
    3
    '''
    if n <= K:
        return 0
    if K == 0:
        return N
    ans = max(N % n - (K - 1), 0)
    ans += N // n * (n - K)
    return ans


def solve(N: int, K: int):
    ans = 0
    for b in range(N+1):
        #print(b, f(b, N, K))
        ans += f(b, N, K)
    return ans


