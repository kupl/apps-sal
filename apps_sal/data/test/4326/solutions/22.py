# ある学校には、N人の生徒がいます。
# 生徒たちをいくつかのグループに分け、グループごとにあるテーマについて話し合ってもらうこととなりました。
# あなたは、2人以下のグループだと効果的な話し合いが出来ないと考えており、なるだけ多くのグループを
# 3人以上にしたいです。生徒たちを上手く分けて、3人以上のグループの数を最大化してください。

# 標準入力から生徒数 N を取得する
input_num = int(input())

# 取得した N人から、3人以上のグループが最大数になるよう算出して出力する
result = int(input_num / 3)
print(result)
