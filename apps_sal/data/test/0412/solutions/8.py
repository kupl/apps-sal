n = int(input())
arr = list(map(int, input().split()))
step = [1, 2, 4, 8, 16, 32, 64, 128, 256, 512, 1024, 2048, 4096, 8192, 16384, 32768, 65536, 131072, 262144, 524288, 1048576, 2097152, 4194304, 8388608, 16777216, 33554432, 67108864, 134217728, 268435456, 536870912]
r = 0
c = 0
for i in range(n):
    for j in range(29, -1, -1):
        if arr[i] % step[j] == 0:
            break
    if j == r:
        c += 1
    if j > r:
        r = j
        c = 1

print(step[r], c)
