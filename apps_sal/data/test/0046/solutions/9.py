(n, m) = map(int, input().split())
k0 = n // 5 * (m // 5)
k1 = (n // 5 + (n % 5 > 0)) * (m // 5 + (m % 5 > 3))
k2 = (n // 5 + (n % 5 > 1)) * (m // 5 + (m % 5 > 2))
k3 = (n // 5 + (n % 5 > 2)) * (m // 5 + (m % 5 > 1))
k4 = (n // 5 + (n % 5 > 3)) * (m // 5 + (m % 5 > 0))
print(k0 + k1 + k2 + k3 + k4)
