def ii():
    return int(input())


def mi():
    return list(map(int, input().split()))


def li():
    return list(mi())


d = {1: 0, 3: 1, 7: 1, 15: 5, 31: 1, 63: 21, 127: 1, 255: 85, 511: 73, 1023: 341, 2047: 89, 4095: 1365, 8191: 1, 16383: 5461, 32767: 4681, 65535: 21845, 131071: 1, 262143: 87381, 524287: 1, 1048575: 349525, 2097151: 299593, 4194303: 1398101, 8388607: 178481, 16777215: 5592405, 33554431: 1082401}
for _ in range(ii()):
    n = ii()
    if n in d:
        ans = d[n]
    else:
        ans = 2 ** n.bit_length() - 1
    print(ans)
