
import sys, itertools

n, m = [int(x) for x in sys.stdin.readline().split()]
lines = [sys.stdin.readline() for _ in range(n)]

classes = [
    [str(x) for x in range(10)],
    [chr(x) for x in range(ord('a'), ord('z')+1)],
    ['#', '*', '&']
]


def steps_to(clazz, line):
    ltr = list(line[:-1])
    rtl = ltr[0:1] + ltr[::-1]
    for i in range(len(ltr)):
        if ltr[i] in clazz or rtl[i] in clazz:
            return i
    return 999999


dists = [
    [steps_to(clazz, line) for clazz in classes]
    for line in lines
]

res = 999999

for perm in itertools.permutations(list(range(3))):

    pick1 = min([(dists[i][perm[0]], i) for i in range(len(dists))])
    pick2 = min([(dists[i][perm[1]], i) for i in range(len(dists)) if i != pick1[1]])
    pick3 = min([(dists[i][perm[2]], i) for i in range(len(dists)) if i != pick1[1] and i != pick2[1]])

    res = min(res, pick1[0]+pick2[0]+pick3[0])

print(res)

