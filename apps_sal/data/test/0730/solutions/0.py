a = ['+------------------------+', '|#.#.#.#.#.#.#.#.#.#.#.|D|)', '|#.#.#.#.#.#.#.#.#.#.#.|.|', '|#.......................|', '|#.#.#.#.#.#.#.#.#.#.#.|.|)', '+------------------------+']
n = int(input())
row = 1
pos = 1
for i in range(n):
    a[pos] = a[pos][:row] + 'O' + a[pos][row + 1:]
    pos += 1
    if row != 1 and pos == 3:
        pos += 1
    if pos > 4:
        pos = 1
        row += 2
for x in a:
    print(x)
