x1, y1 = map(int, input().split())
x2, y2 = map(int, input().split())
x3, y3 = map(int, input().split())
ans = set()
x4 = x3 + (x1 - x2)
y4 = y3 - (y2 - y1)
x5 = x3 - (x1 - x2)
y5 = y3 + (y2 - y1)
x6 = x1 - (x3 - x2)
y6 = y1 - (y3 - y2)
ans.add((x4, y4))
ans.add((x5, y5))
ans.add((x6, y6))
print(len(ans))
for i in ans:
    print(*i)
