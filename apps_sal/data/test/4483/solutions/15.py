# あなたは、X 円を持ってケーキとドーナツを買いに出かけました。
# あなたはまずケーキ屋で 1個 A円のケーキを 1個買いました。
# 次に、ドーナツ屋で 1個 B 円のドーナツをできるだけたくさん買いました。
#
# これらの買い物のあと手元に残っている金額は何円ですか。

# 制約
# 1 ≦ A, B ≦ 1,000
# A + B ≦ X ≦ 10,000
# X, A, B は整数である

# 標準入力から、X, A, B の値を取得する
x = int(input())
a = int(input())
b = int(input())

# 残金を計算して出力する
result = 0

result = (x - a) % b
print(result)
