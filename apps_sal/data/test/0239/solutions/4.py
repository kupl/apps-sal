def dist(v):
    ans = 0
    for i in range(4):
        for j in range(4):
            if i != j and v[i] == v[j]:
                return 0
    for i in range(3):
        ans += (v[i][0] - v[i + 1][0]) ** 2 + (v[i][1] - v[i + 1][1]) ** 2
    return ans
